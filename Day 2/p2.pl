sub _calc_total {
    my ($arr) = @_;
    #say scalar(@$arr);
    for( my $i = 0; $i < scalar @$arr ; $i = $i + 4 ) {
        if( $arr->[$i] == 1 || $arr->[$i] == 2) {
            my $first = $arr->[$arr->[$i+1]];
            my $second = $arr->[$arr->[$i+2]];
            my $position = $arr->[$i+3];
            my $val = 0;
            if( $arr->[$i] == 1 ) { 
                $val = $first + $second;
            }
            else {
                $val = $first * $second;
            }
            $arr->[$position] = $val;
        }
        elsif( $arr->[$i] == 99 ) {
            return  0;
        }
    }
    return 0;
}


